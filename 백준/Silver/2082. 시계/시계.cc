#include<bits/stdc++.h>
using namespace std;

int main(void) {
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
    string num[5][10] = {
        {{"###"}, {"..#"}, {"###"}, {"###"}, {"#.#"}, {"###"}, {"###"}, {"###"}, {"###"}, {"###"}},
        {{"#.#"}, {"..#"}, {"..#"}, {"..#"}, {"#.#"}, {"#.."}, {"#.."}, {"..#"}, {"#.#"}, {"#.#"}},
        {{"#.#"}, {"..#"}, {"###"}, {"###"}, {"###"}, {"###"}, {"###"}, {"..#"}, {"###"}, {"###"}},
        {{"#.#"}, {"..#"}, {"#.."}, {"..#"}, {"..#"}, {"..#"}, {"#.#"}, {"..#"}, {"#.#"}, {"..#"}},
        {{"###"}, {"..#"}, {"###"}, {"###"}, {"..#"}, {"###"}, {"###"}, {"..#"}, {"###"}, {"..#"}},
    };

    string s[5];
    for(int i=0;i<5;i++) {
        getline(cin, s[i]);
    }

    for(int i=0;i<15;i+=4) {
        for(int cnt=0;cnt<10;cnt++) {
            bool chk=true;
            for(int j=0;j<5;j++) {
                for(int k=0;k<3;k++) {
                    if(s[j][i+k]=='#' && num[j][cnt][k]!='#') {
                        chk=false;
                    }
                }
            }

            if(chk) {
                cout << cnt;
                if(i==4) cout << ':';
                break;
            }
        }
    }
}